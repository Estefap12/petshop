function img12(anything) {
    document.querySelector('.slide12').src = anything;
  }

   function changes(change) {
    const line = document.querySelector('.home');
    line.style.background = change;
  }

   let openModal12 = document.querySelector('.dog12');
   let modal12 = document.querySelector('.modal12');
   let closeModal12 = document.querySelector('.modal_close12');
   
   openModal12.addEventListener('click', (a)=>{
       a.preventDefault();
       modal12.classList.add('modal--show12');
   });
   
   closeModal12.addEventListener('click', (a)=>{
       a.preventDefault();
       modal12.classList.remove('modal--show12');
   });
 
